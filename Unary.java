class Unary{

 public static void main(String[] args){

  int m = 13;
  int n = 14;
   System.out.println(m);
   System.out.println(m++);
   System.out.println(m);
   System.out.println(++m);
   System.out.println(m);
   System.out.println(m--);
   System.out.println(m);
   System.out.println(--m);
   System.out.println(m);
   System.out.println(m);
   System.out.println(n);
   System.out.println(m++ + n++);
   System.out.println(++m + ++n);
   System.out.println(m++ + ++n);
   System.out.println(++m + n++);
   System.out.println(m);
   System.out.println(n);
   System.out.println(m++ + n--);
   System.out.println(--m - ++n);
   System.out.println(m++ - --n);
   System.out.println(--m - n++);
   System.out.println(m);
   System.out.println(n);
}

}